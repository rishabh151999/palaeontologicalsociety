         <script>
            function toggleAccordion(el) {
               const content = el.nextElementSibling;
               const icon = el.querySelector(".icon");

               if (content.classList.contains("open")) {
                  content.classList.remove("open");
                  icon.textContent = "+";
               } else {
                  content.classList.add("open");
                  icon.textContent = "−";
               }
            }
         </script>

