<section id="paragraph" class="wrapper paragraph-wrapper">
  <div class="container common-container four_content">

    <h3>A. Constitution</h3>
    <p>1. The Society shall consist of Patrons, Honorary Fellows and Fellows.</p>
    <p>2. Patrons shall be men of learning and eminence and shall be duly elected by the Executive Council.</p>
    <p>3. Honorary Fellows shall be selected from among eminent scientists or other persons in India or abroad who have rendered service to the cause of palaeontology and allied branches of science.</p>
    <p>4. Fellows shall be persons enrolled in accordance with the regulations of the Society.</p>
    <p>5. The Headquarters of the Society shall be located at the Department of Geology, University of Lucknow, Lucknow – 226007.</p>

    <hr />

    <h3>B. Privileges</h3>
    <p>6. Fellows shall have the right to vote, nominate and stand for election to the office of the Society. The publications of the Society shall be supplied free to all Fellows.</p>

    <hr />

    <h3>C. Elections</h3>
    <p>7. Candidates for Fellowship shall be proposed and seconded by existing Fellows and approved by the Council.</p>
    <p>8. No person shall exercise the rights of Fellowship until payment of admission and annual fees within three months of election.</p>

    <hr />

    <h3>D. Subscription</h3>
    <ul>
      <li>Indian Fellow: ₹100 admission fee and ₹1000 annual subscription.</li>
      <li>Foreign Fellow: US $15 admission fee and US $40 annual subscription.</li>
      <li>Life Fellowship: ₹8000 + ₹500 admission fee or US $220.</li>
      <li>Failure to pay subscription for three years results in loss of privileges.</li>
    </ul>

    <hr />

    <h3>E. Subventions</h3>
    <p>13. The Society may accept grants from Central/State Governments and other institutions.</p>

    <hr />

    <h3>F. Council and Its Officers</h3>
    <p>14. The General Body is the supreme authority. Administration shall be carried out by an Executive Council consisting of:</p>

    <ul>
      <li>President</li>
      <li>Two Vice-Presidents</li>
      <li>Secretary</li>
      <li>Two Joint Secretaries</li>
      <li>Treasurer</li>
      <li>Nine Fellows</li>
    </ul>

    <p>At least one Vice President, Secretary, Treasurer and two Council Members shall be residents of Lucknow.</p>

    <p>15–22. The Council shall oversee elections, financial management, auditing, and administration of the Society.</p>

    <hr />

    <h3>G. Publications</h3>
    <p>23. The Society shall publish the Journal of the Palaeontological Society of India and other publications as approved by the Council.</p>

    <hr />

    <h3>H. Meetings</h3>
    <p>24. The Annual General Body Meeting shall be held at least once a year. Quorum requirements shall apply as prescribed.</p>
    <p>25. Emergency meetings may be convened by the President or Secretary as required.</p>

    <hr />

    <h3>I. Properties</h3>
    <p>26. The President or Council shall be responsible for all properties of the Society.</p>

    <hr />

    <h3>J. Amendments</h3>
    <p>27. Amendments to the Constitution may be made in the General Body Meeting with due notice and quorum.</p>

    <hr />

    <h3>K. Rules of Business</h3>
    <p>28. The Council shall frame rules of procedure subject to ratification by the General Body.</p>

